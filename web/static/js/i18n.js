const LANG_LABELS = {
  en: 'English',
  fr: 'Français',
  it: 'Italiano',
  es: 'Español',
  de: 'Deutsch',
};

class I18n {
  constructor() {
    this.currentLang = localStorage.getItem(I18N_STORAGE_KEY) || this.detectLang();
    if (!translations[this.currentLang]) this.currentLang = 'en';
  }

  detectLang() {
    const browser = (navigator.language || 'en').slice(0, 2).toLowerCase();
    return translations[browser] ? browser : 'en';
  }

  t(key, vars) {
    const dict = translations[this.currentLang] || translations.en;
    let str = dict[key] || translations.en[key] || key;
    if (vars) {
      for (const k in vars) str = str.replace(`{${k}}`, vars[k]);
    }
    return str;
  }

  /** Same as t() but returns null when the key is not in any dict. Lets
   *  applyTranslations preserve the inline HTML fallback rather than
   *  display the raw key (`capi.tab.install`) on screen. */
  _tIfPresent(key) {
    const dict = translations[this.currentLang] || translations.en;
    if (dict && dict[key]) return dict[key];
    if (translations.en[key]) return translations.en[key];
    return null;
  }

  setLang(lang) {
    if (!translations[lang]) return;
    this.currentLang = lang;
    localStorage.setItem(I18N_STORAGE_KEY, lang);
    document.documentElement.lang = lang;
    this.applyTranslations();
  }

  applyTranslations() {
    // If a key is missing from every dict, keep whatever the HTML already
    // had (the author-provided fallback) instead of stamping the raw key
    // — that's the i18n best practice and what fixed "capi.tab.install"
    // appearing on-screen.
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const tr = this._tIfPresent(el.getAttribute('data-i18n'));
      if (tr !== null) el.textContent = tr;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const tr = this._tIfPresent(el.getAttribute('data-i18n-title'));
      if (tr !== null) el.title = tr;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const tr = this._tIfPresent(el.getAttribute('data-i18n-placeholder'));
      if (tr !== null) el.placeholder = tr;
    });
    // data-tip-i18n="key" → resolve into data-tip="<translated string>",
    // which the CSS tooltip system (.tip[data-tip]::after) reads via
    // attr(data-tip). Without this step the bubble never appears even
    // though the markup looks correct. (Bug discovered 2026-05-29.)
    document.querySelectorAll('[data-tip-i18n]').forEach(el => {
      const tr = this._tIfPresent(el.getAttribute('data-tip-i18n'));
      if (tr !== null) el.setAttribute('data-tip', tr);
    });
    // v1.4.17 a11y: any .tip element that isn't naturally focusable
    // (i.e. not a button/link/input) needs tabindex=0 so keyboard users
    // can Tab to it and see the tooltip via :focus-visible. Idempotent —
    // safe to call on every render.
    document.querySelectorAll('.tip[data-tip]').forEach(el => {
      const tag = el.tagName.toLowerCase();
      if (tag === 'button' || tag === 'a' || tag === 'input' ||
          tag === 'select' || tag === 'textarea') return;
      if (!el.hasAttribute('tabindex')) el.setAttribute('tabindex', '0');
      if (!el.hasAttribute('role')) el.setAttribute('role', 'note');
      // aria-label mirrors the tooltip text so screen readers announce
      // it. The visual tooltip is purely supplementary.
      if (!el.hasAttribute('aria-label')) {
        el.setAttribute('aria-label', el.getAttribute('data-tip'));
      }
    });
    document.title = this.t('header.title');
    document.documentElement.lang = this.currentLang;
  }

  getAvailableLanguages() {
    return Object.keys(translations).map(k => ({ code: k, label: LANG_LABELS[k] }));
  }
}

const i18n = new I18n();
function t(key, vars) { return i18n.t(key, vars); }

// v1.4.23: a top-level `const` in a classic <script> creates a binding
// reachable as a bare identifier from OTHER classic scripts (app.js,
// capi.js, …) but is INVISIBLE to ES modules (topology.js, notes.js)
// — they only see globals through `window`. Expose explicitly so any
// kind of consumer can resolve i18n the same way.
if (typeof window !== 'undefined') {
  window.i18n = i18n;
  window.t    = t;
}
